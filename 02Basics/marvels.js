//Datatype - Array

//Usually use const for arrays
const superHeroes = ['Iron Man', 'Hulk', 'Captain America', 'Thor']
/*Can include a variety of data types in single array*/
console.log(superHeroes);
console.log(superHeroes[0]);
console.log(superHeroes[(superHeroes.length-1)]);

console.log(`We have ${superHeroes.length} super heroes `)
/*Tildas allows us to specify the inclusion of any
JS code within our string with the use ${}*/
console.log(`The last superhero is ${superHeroes[(superHeroes.length-1)]}`)

